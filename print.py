a = 16.034561
m = "ตารางหน่วย"

print("พื้นืี่สี่เหลี่ยม ="+str(a)+m)
print("พื้นืี่สี่เหลี่ยม =",a,m)
print("พื้นืี่สี่เหลี่ยม =\n",a)
print("พื้นืี่สี่เหลี่ยม =\t",a,m)
print("พื้นืี่สี่เหลี่ยม ={:.2f}{}".format(a,m))     #ลำดับการแสดงผล {:.2f} อันนี้แสดงทศนิยม2ตำแหน่ง(อันนี้แสดงผลa)

