word = input("ใส่ข้อความที่คุณต้องการ :")
n = 0
z = 0
y = 0
for a in word :
    n += 1
    if a == "1" or a == "3" or a == "5" or a == "7" or a == "9" :
        print(f"{n}. {a} เป็นตัวเลขแต่ไม่ใช่เลขคู่")
        z += 1
    elif a == "2" or a == "4" or a == "6" or a == "8"  :
        print(f"{n}. {a} เป็นตัวเลขคู่")
        y += 1
    else :
        print(f"{n}. {a} ไม่ใช่ตัวเลข")
print(f"""
ข้อความ {word} ของคุณ
มีเลขคู่ทั้งหมด {z} ตัว
ไม่ใช่เลขคี่ทั้งหมด {n-y} ตัว
""")